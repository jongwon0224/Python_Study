# 자료형이란 => 데이터의 종류
'''
자료형 결정 x => 파이썬 내부에서 자동으로 적용 (자바스크립트처럼)
자료형 확인 => type()함수로 알수있음
'''
a,b = 1,2

print(a + b) # 3
print(a - b) # -1
print(a * b) # 2
print(a / b) # 0.5
print(a // b) # 0 -> a를 b로 나눈 몫
print(a % b) # 1 -> 나머지
print(a ** b) # 1 -> a의 b 제곱

print(4/2) # 2.0 -> float타입으로 출력됨 (Java처럼 4+2.0같이 강제 형변환도 되는듯)

